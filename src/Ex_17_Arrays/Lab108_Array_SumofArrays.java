package Ex_17_Arrays;

public class Lab108_Array_SumofArrays {
    public static void main(String[] args) {
        int[] numbers = {18,26,30};

        int sum = 0;

        for (int i = 0; i < numbers.length ; i++) {
            sum = sum + numbers[i];

        }
        System.out.println(sum);
        }


    }

