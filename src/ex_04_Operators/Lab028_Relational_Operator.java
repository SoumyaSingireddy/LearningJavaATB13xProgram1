package ex_04_Operators;

public class Lab028_Relational_Operator {
    public static void main(String[] args) {
        //< Less Than
        // <= -> Less than or equal to
        // > Greater
        // >= greater or equal
        // != ->Not equal

        // All of them result boolean output.

        int a = 10;
        int b = 30;
        boolean c = a>b; //10>30
        System.out.println(c);

        int age_Soumya = 30;
        int age_Teena = 35;
        boolean result = age_Teena >= age_Soumya;
        System.out.println(result);


    }
}
