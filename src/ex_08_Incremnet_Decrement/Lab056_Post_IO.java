package ex_08_Incremnet_Decrement;

public class Lab056_Post_IO {
    public static void main(String[] args) {
        int a = 10 ;
        System.out.println(++a);
        System.out.println(a);

        // Post increment = Print first and then increase

        int a_post = 10;
        System.out.println(a_post++);
        System.out.println(a_post);
    }
}
